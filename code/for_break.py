import random

# else после for выполняется только если цикл завершился без break.
# В этом примере, если для текущего i не было ни одного break (т.е. все 20 раз выпал "орёл"),
# выполняется блок else и выводится "удачный" сид.
# Если break был вызван хотя бы раз, else не выполняется для этого i.

for i in range(1_000_001):
    random.seed(i)
    for j in range(20):
        if not random.randrange(2):
            break
    else:
        print(i)
        break
else:
    print("Среди чисел от 0 до 1000000 нет подходящих")

# Да, else для цикла выполняется только если цикл завершился естественно (без break).
# Если цикл был прерван через break, блок else не выполняется.
# Это работает одинаково для for и while.
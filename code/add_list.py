x = [1, 2, 3]
y = x
# x и y указывают на один и тот же объект списка
print(x == y)
print(id(x))
print(id(y))
x = x + [1]
# x теперь указывает на новый объект (результат x + [1]), а y остался прежним
print(x)
print(y)
print(id(x))  # id(x) изменился, потому что x теперь новый объект

print(id(y))
